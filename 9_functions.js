const capitaliseWord = word => `${word.charAt(0).toUpperCase()}${word.substring(1)}`;

console.log(capitaliseWord("hello")); // Output: 'Hello'
console.log(capitaliseWord("mom")); // Output: 'Mom'
console.log(capitaliseWord("dAD")); // Output: 'DAD'

